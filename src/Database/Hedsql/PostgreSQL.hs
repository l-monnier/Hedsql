PostgreSQL implementation including constructor, code generator and driver.

Using this module, you will be able to code generator queries specifically for
PostgreSQL such as:

@
   select currentDate
|> from (table "table1")
|> end
@

> SELECT CURRENT_DATE FROM "table1"

This module also loads functionalities specific to PostgreSQL to write queries
such as:

@
   select "col1"
|> from (lateral (select "col1") "t1")
@

> SELECT "col1" FROM LATERAL (SELECT "col1") AS "t1"

Such queries cannot have code generated by another code generator than the
PostgreSQL one.
