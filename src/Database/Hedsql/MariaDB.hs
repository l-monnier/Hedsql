MariaDB implementation including constructor, code generator and driver.

Using this module, you will be able to generate queries for MariaDB.

To write queries which code cannot be generated by the other drivers, you can
import the constructors dedicated to MariaDB:

> import Hedsql.Drivers.MariaDB.Constructor

You can then write queries as for example:

@
   select [colRef calcFoundRows, colRef (//*)]
|> from (table "table1")
|> end
@

> SELECT * CALC_FOUND_ROWS FROM `table1`
